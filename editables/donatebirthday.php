<h1>Donate Your Birthday</h1>
<br/>

<p>Instead of material gifts, ask your friends to join together in supporting Zidisha entrepreneurs to commemorate your upcoming birthday or other holiday.  Here's how:
<br/><br/>
1. <a href="https://www.zidisha.org/index.php?p=81" target="_blank">Create a lending group</a> for your birthday or holiday.
<br/><br/>
2. <a href="https://www.zidisha.org/index.php?p=30" target="_blank">Invite your friends</a> to join the group and make a loan in lieu of a traditional gift. 
<br/><br/>
3. Celebrate the positive difference you have made through your birthday or holiday, as your group's total lending volume goes up with each new contribution.</p>