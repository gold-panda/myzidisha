			</li>
			<li>
				We had an all-volunteer staff through the end of 2011.  Our only non-program costs were website development and legal registration fees.  The rest of our expenditures were for making loans: transferring funds to borrowers, webhosting, telephone calls, and background checks for new borrowers.
			</li>
			<li>
				Over the course of 2011, donations to Zidisha leveraged more than thirteen times their value in new loan disbursements.
